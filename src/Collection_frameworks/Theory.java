package Collection_frameworks;

public class Theory {
/*

Collection Framework = group of objects
a ready-made architecture or "toolkit" that provides predefined interfaces, classes, and algorithms
to store, organize, and manipulate a group of objects efficiently

=> Main interfaces:

1. List
   - Allows duplicates
   - Maintains insertion order

   a) ArrayList
      - Dynamic Array
      - Fast random access
      - Slow insertion/deletion in middle

   b) LinkedList
      - Doubly Linked List
      - Better insertion/deletion
      - Slow random access

2. Set
   - Stores unique elements

   a) HashSet
      - No insertion order
      - Allows one null element

   b) TreeSet
      - Maintains sorted order
      - Does not allow null elements

3. Map (Not a child of Collection interface)
   - Stores key-value pairs
   - Keys are unique
   - Duplicate values allowed

   a) HashMap
      - No ordering
      - One null key
      - Multiple null values

   b) TreeMap
      - Maintains keys in sorted order
      - No null keys
      - Multiple null values allowed


*/
}
