package Intermediate_and_Terminal_Operations;

public class Theory {
    /*
    Terminal Operations =>
    These operations transform a stream into another stream.
    They process operations on each element of the stream, like filtering, mapping, etc.
    They are lazy, means they are not executed until a terminal operation is invoked.
    Example = filter(), sorted(), map(), flatMap(), distinct(), skip(), limit(), etc.


    Terminal Operations =>
    these operations trigger the processing of the stream and produce a end result.
    Once a terminal operation is executed, the stream cannot be resued
    Examples= collect(), forEach(), reduce(), count(), min(), max(), anyMatch(),
    allMatch(), noneMatch(), findFirst, findAny(), etc.


     */
}
